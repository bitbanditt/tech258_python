#Operators

x = 5
y = 1

#arithmetic operators

result = x + y + 2
print("Let's show x + y + 2 =", result)
result = x - y - 2
print("Let's show x - y - 2 =", result)
result = x * y * 2
print("Let's show x * y * 2 =", result)
result = x / y / 2
print("Let's show x / y / 2 =", result)

# % modulo --> reminder of division
result = x % y % 2
print("Let's show x % y % 2 =", result)

print(5 % 4) # left over

# Comparison operators - >, <, ==, !=, >=, <=

result = x > y # should be true
print("Let's show x > y =", result)
print(type(result))
result = x < y
